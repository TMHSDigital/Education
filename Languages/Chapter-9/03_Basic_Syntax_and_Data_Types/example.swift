// Example of Basic Operators
let a = 10
let b = 5
print(a + b)  // Addition
print(a - b)  // Subtraction
print(a > b)  // Greater than
print(a == b) // Equal to
print(a > b && b < 10) // Logical AND
print(a > b || b > 10) // Logical OR


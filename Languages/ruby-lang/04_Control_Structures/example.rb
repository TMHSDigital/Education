# Example of Control Structures

x = 10
if x > 5
  puts "x is greater than 5"
elsif x == 5
  puts "x is 5"
else
  puts "x is less than 5"
end

# While Loop
count = 0
while count < 5
  puts count
  count += 1
end

# Until Loop
count = 0
until count == 5
  puts count
  count += 1
end

# For Loop
for i in 0..4
  puts i
end

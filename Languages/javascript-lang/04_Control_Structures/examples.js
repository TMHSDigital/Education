// Example of Conditional Statements
let x = 10;
if (x > 5) {
    console.log("x is greater than 5");
} else if (x === 5) {
    console.log("x is 5");
} else {
    console.log("x is less than 5");
}

// Example of For Loop
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// Example of While Loop
let count = 0;
while (count < 5) {
    console.log(count);
    count++;
}


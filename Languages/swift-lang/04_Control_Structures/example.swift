// Example of Control Structures

let x = 10
if x > 5 {
    print("x is greater than 5")
} else if x == 5 {
    print("x is 5")
} else {
    print("x is less than 5")
}

// For Loop
for i in 0..<5 {
    print(i)
}

// While Loop
var count = 0
while count < 5 {
    print(count)
    count += 1
}

// Repeat-While Loop
count = 0
repeat {
    print(count)
    count += 1
} while count < 5

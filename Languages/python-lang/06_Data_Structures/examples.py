# Example of Lists
fruits = ["apple", "banana", "cherry"]
print(fruits[0])  # Accessing elements
fruits.append("orange")  # Adding an element
print(fruits)
# Example of Tuples
person = ("John", 30)
print(person[0])
# Example of Dictionaries
student = {"name": "Alice", "age": 25}
print(student["name"])
student["grade"] = "A"
print(student)
# Example of Sets
colors = {"red", "green", "blue"}
colors.add("yellow")
print(colors)

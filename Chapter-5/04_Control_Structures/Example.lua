-- Example of Conditional Statements

x = 10
if x > 5 then
    print("x is greater than 5")
elseif x == 5 then
    print("x is 5")
else
    print("x is less than 5")
end

-- Example of For Loop
for i = 0, 4 do
    print(i)
end

-- Example of While Loop
count = 0
while count < 5 do
    print(count)
    count = count + 1
end


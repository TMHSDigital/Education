-- Example of Basic Operators
a = 10
b = 5
print(a + b)  -- Addition
print(a - b)  -- Subtraction
print(a > b)  -- Greater than
print(a == b) -- Equal to
print(a > b and b < 10) -- Logical AND
print(a > b or b > 10)  -- Logical OR

